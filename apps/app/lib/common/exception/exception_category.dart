/// エラーカテゴリの定義
///
/// エラーハンドリングガイドラインに基づいて、
/// エラーを3つのカテゴリに分類する。
enum ExceptionCategory {
  /// 業務例外：ユーザー修正可能
  ///
  /// ユーザーの操作や入力によって発生し、
  /// ユーザー自身が対処可能なエラー。
  /// 例：バリデーションエラー、権限不足、重複エラー
  logic,

  /// システム例外：一時的障害
  ///
  /// システムの一時的な問題によって発生し、
  /// 再試行や時間をおくことで解決する可能性があるエラー。
  /// 例：ネットワークエラー、タイムアウト、外部API障害
  system,

  /// 致命的エラー：回復不可能
  ///
  /// アプリケーションが正常に動作できない状態で、
  /// ユーザーが対処できないエラー。
  /// 例：データ破損、セキュリティ違反、予期しないエラー
  fatal,
}
